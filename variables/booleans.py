# Booleans
is_human = False
is_raining = True
is_password = False

print(f"Are you human?: {is_human}")


if is_human:
    print("Yes, I am human!")
else:
    print("No, I am an alien!")


if is_raining:
    print("It is raining outside.")
else:
    print("It is NOT raining outside.")

if is_password:
    print("Password Accepted!")
else:
    print("Password Failed!")
