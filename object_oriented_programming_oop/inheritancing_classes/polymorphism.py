# Polymorphism

# Notes

# polymorphism 
    # - means to have many forms/faces
    # - poly: many
    # - morph: form

# objects can take 1 of many forms

# 2 ways to make polymorphism happen 
    # - inheritance 
    # - duck typing


# parent class
class Shape():
    pass

# child classes
class Pentagon(Shape):
    pass

class Hexagon(Shape):
    pass

class Nonagon(Shape):
    pass

class Decagon(Shape):
    pass


# objects made from classes
pentagon = Pentagon()

hexagon = Hexagon()

nonagon = Nonagon()

decagon = Decagon()

